package Model;

/**
 *
 * @author microsoft
 */
public class MBuku {
 String kodebuku, kodejenis, kodekategori, judul, pengarang, kodepenerbit, tahun;
 int jumlahbuku;

    public String getKodebuku() {
        return kodebuku;
    }

    public void setKodebuku(String kodebuku) {
        this.kodebuku = kodebuku;
    }

    public String getKodejenis() {
        return kodejenis;
    }

    public void setKodejenis(String kodejenis) {
        this.kodejenis = kodejenis;
    }

    public String getKodekategori() {
        return kodekategori;
    }

    public void setKodekategori(String kodekategori) {
        this.kodekategori = kodekategori;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getPengarang() {
        return pengarang;
    }

    public void setPengarang(String pengarang) {
        this.pengarang = pengarang;
    }

    public String getKodepenerbit() {
        return kodepenerbit;
    }

    public void setKodepenerbit(String kodepenerbit) {
        this.kodepenerbit = kodepenerbit;
    }

    public String getTahun() {
        return tahun;
    }

    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    public int getJumlahbuku() {
        return jumlahbuku;
    }

    public void setJumlahbuku(int jumlahbuku) {
        this.jumlahbuku = jumlahbuku;
    }
 
 
}
